  public final void StallNum(){
      
      try{
      con1 =  (java.sql.Connection) Connections.proper.getMySqlConnection();
      con2 =  (java.sql.Connection) Connections.Connection.getMySqlConnection();
      
      String insert = "Select * from Stall";
      pst = (PreparedStatement) con1.prepareStatement(insert);
      pst = (PreparedStatement) con2.prepareStatement(insert);
      rs = pst.executeQuery(insert);
      while(rs.next()){
      String StallNumber = rs.getString("Stall_Num");
      String sta = rs.getString("Stall_Status");
      if(sta.equals("Available")){
      addStallCombo.addItem(StallNumber);
       }
      }con1.close();
      con2.close();
      }catch(Exception e){
      JOptionPane.showMessageDialog(null,e);
      }}
