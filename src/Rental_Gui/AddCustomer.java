    private void addStallComboItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_addStallComboItemStateChanged
       PreparedStatement pst = null;
       ResultSet rs = null;
       String Stall = (String) addStallCombo.getSelectedItem();
       if(Stall.equals("Choose Stall Number:")){
       stallname.setText("");
       stallLoc.setText("");
       stallsec.setText("");
       }
       try{
       con1 =  (java.sql.Connection) Connections.proper.getMySqlConnection();
        con2 =  (java.sql.Connection) Connections.Connection.getMySqlConnection();
         
       String insert = "Select * from Stall INNER JOIN Section On Stall.Section_Num = Section.Section_Num where Stall.Stall_Num = ?";
       pst =con1.prepareStatement(insert);
       pst =con2.prepareStatement(insert);
       pst.setString(1,Stall);
       rs = pst.executeQuery();
       
       if (rs.next()){
       String name= rs.getString("Stall_Name");
       stallname.setText(name);
       String location= rs.getString("Section_Description");
       stallLoc.setText(location);
       String section= rs.getString("Section_Type");
       stallsec.setText(section);
       } 
       }
       catch(Exception e){
       JOptionPane.showMessageDialog(null,"ERROR","ERROR",JOptionPane.ERROR_MESSAGE); 
       }
    }//GEN-LAST:event_addStallComboItemStateChanged

